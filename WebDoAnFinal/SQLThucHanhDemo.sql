
--BANG SAN PHAM
CREATE TABLE SANPHAM (
MASP CHAR(9) NOT NULL,
MALOAI CHAR(9) NOT NULL,
MANSX CHAR(9) NOT NULL,
TENSP NVARCHAR(40),
MOTA NVARCHAR(20),
HINHCHINH CHAR(9),
HINHPHU CHAR(9),
GIA MONEY,
SOLUONGDABAN INT,
LUOTVIEW INT,
TINHTRANG CHAR(9),
CONSTRAINT PK_SP PRIMARY KEY (MASP)
)

--BANG NHA SAN XUAT
CREATE TABLE NHASANXUAT (
MANSX CHAR(9) NOT NULL,
TENNSX NVARCHAR(40),
CONSTRAINT PK_NHASANXUAT PRIMARY KEY (MANSX)
)

--BANG LOAI SAN PHAM
CREATE TABLE LOAISP (
MALOAI CHAR(9),
TENLOAI NVARCHAR(40),
CONSTRAINT PK_LOAISP PRIMARY KEY (MALOAI)
)
--KHOA NGOAI
ALTER TABLE SANPHAM ADD CONSTRAINT FK1_SP FOREIGN KEY(MALOAI) REFERENCES LOAISP(MALOAI)
ALTER TABLE SANPHAM ADD CONSTRAINT FK2_SP FOREIGN KEY(MANSX) REFERENCES NHASANXUAT(MANSX)


